"""
NPZ episode loading.

This module is the single point of contact between raw .npz files on disk
and the rest of the framework.  All field name assumptions and joint-ordering
logic live here, so changing the data format only requires edits in this file.

Expected NPZ structure
----------------------
Required fields:
    joint_positions     (T,) array of dicts   OR  (T, 30) float array
    joint_efforts       (T, 30) float array
    joint_velocities    (T, 30) float array

Optional visual fields (present when cameras are used):
    images_cam0 … images_cam{N-1}   (T, H, W, 3) uint8
    depth_cam0  … depth_cam{N-1}    (T, H, W) uint16 / float

Optional tactile:
    tactile_states   (T, 60) float

Optional pre-computed visual features (generated by the pre-computation
script, saved alongside the NPZ):
    rgb_features_cam0 … rgb_features_cam{N-1}  (T, raw_dim) float
"""

from __future__ import annotations

import os
import concurrent.futures
from dataclasses import dataclass
from pathlib import Path
from typing import Iterator

import natsort
import numpy as np

# ---------------------------------------------------------------------------
# Joint ordering (30-DOF Shadow Hand + UR arm system)
# ---------------------------------------------------------------------------

_JOINT_ORDER = [
    # UR arm (6 DOF)
    "ra_shoulder_pan_joint", "ra_shoulder_lift_joint", "ra_elbow_joint",
    "ra_wrist_1_joint",      "ra_wrist_2_joint",       "ra_wrist_3_joint",
    # Shadow Hand (24 DOF)
    "rh_FFJ1", "rh_FFJ2", "rh_FFJ3", "rh_FFJ4",
    "rh_MFJ1", "rh_MFJ2", "rh_MFJ3", "rh_MFJ4",
    "rh_RFJ1", "rh_RFJ2", "rh_RFJ3", "rh_RFJ4",
    "rh_LFJ1", "rh_LFJ2", "rh_LFJ3", "rh_LFJ4", "rh_LFJ5",
    "rh_THJ1", "rh_THJ2", "rh_THJ3", "rh_THJ4", "rh_THJ5",
    "rh_WRJ1", "rh_WRJ2",
]
assert len(_JOINT_ORDER) == 30, "Joint order must have exactly 30 joints."

ARM_SLICE  = slice(0, 6)    # indices for UR arm joints
HAND_SLICE = slice(6, 30)   # indices for Shadow Hand joints


# ---------------------------------------------------------------------------
# Joint position extraction
# ---------------------------------------------------------------------------

def _extract_joint_array(raw: np.ndarray, default_key: str | None = None) -> np.ndarray:
    """
    Convert a raw joint array from an NPZ file to a (T, 30) float32 array.

    Handles two formats:
      - (T,) array of dicts  — each dict maps joint name → float value
      - (T, D) float array   — already dense; pads/clips to 30 dims

    *default_key* is used when a dict value is itself a dict (e.g.
    ``{"position": 1.23}``).  For ``joint_positions`` pass ``"position"``;
    for other fields it is rarely needed.
    """
    if raw.ndim == 1 and isinstance(raw[0], dict):
        rows = []
        for d in raw:
            if d is None:
                d = {}
            row = []
            for j in _JOINT_ORDER:
                v = d.get(j, 0.0)
                if isinstance(v, dict):
                    v = v.get(default_key, 0.0) if default_key else 0.0
                row.append(float(v))
            rows.append(row)
        return np.array(rows, dtype=np.float32)

    arr = np.asarray(raw, dtype=np.float32)
    if arr.ndim == 1:
        arr = arr[np.newaxis, :]   # single timestep
    T, D = arr.shape
    if D != 30:
        padded = np.zeros((T, 30), dtype=np.float32)
        padded[:, : min(D, 30)] = arr[:, : min(D, 30)]
        return padded
    return arr


def _extract_joint_positions(raw: np.ndarray) -> np.ndarray:
    """Convenience wrapper — extracts joint positions (T, 30)."""
    return _extract_joint_array(raw, default_key="position")


